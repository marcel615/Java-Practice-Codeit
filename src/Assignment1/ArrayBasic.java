package Assignment1;

public class ArrayBasic {
    public static void main(String[] args) {
        // 배열 선언 및 초기화
        int[] arr = {10, 20, 30, 40, 50};

        for (int i = 0; i < arr.length; i++) {
            System.out.println("인덱스 " + i + ": " + arr[i]);
        }

        // 배열 요소 출력
    }
}

